package ua.nure.thao.Practice7.entity;

public class Deposit {
	
	private String name;
	private String country;
	private String depositor;
	private String accountId;
	private String type;
	private String amountOnDeposit;
	private String profitability;
	private String timeConstraints;
	
	public void setName(String name) { this.name = name;}
	public String getName() { return this.name;}
	
	public void setCountry(String country) { this.country = country;}
	public String getCountry() { return this.country;}
	
	public void setDepositor(String depositor) { this.depositor = depositor;}
	public String getDepositor() { return this.depositor;}
	
	public void setAccountId(String accountId) {this.accountId = accountId;}
	public String getAccountId() { return this.accountId;}
	
	public void setType(String type) { this.type = type;}
	public String getType() { return this.type;}
	
	public void setAmountOnDeposit(String amount) { 
		this.amountOnDeposit = amount;
	}
	public String getAmountOnDeposit() { return this.amountOnDeposit;}
	
	public void setProfitability(String prof) { this.profitability = prof;}
	public String getProfitability() { return this.profitability;}
	
	public void setTimeConstraints(String duration) { 
		this.timeConstraints = duration;
	}
	public String getTimeConstraints() { return this.timeConstraints;}
	
	public String toString() {
		StringBuilder sb = new StringBuilder("");
		sb.append("Name: ").append(this.name).append('\n');
		sb.append("Country: ").append(country).append('\n');
		sb.append("Depositor: ").append(depositor).append('\n');
		sb.append("AccounId: ").append(accountId).append('\n');
		sb.append("Type: ").append(type).append('\n');
		sb.append("Amount on deposit: ").append(amountOnDeposit).append('\n');
		sb.append("Profitability: ").append(profitability).append('\n');
		sb.append("Time constraints: ").append(timeConstraints);
		return sb.toString();
	}
}