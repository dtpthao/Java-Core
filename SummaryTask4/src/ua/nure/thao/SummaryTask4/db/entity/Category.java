package ua.nure.thao.SummaryTask4.db.entity;

public class Category extends Entity{

	private static final long serialVersionUID = -6294091263537819321L;
	
	private String name;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	@Override
	public String toString() {
		return "Category [name=" + name + ", id=" + this.getId() + "]";
	}

}
